public class Enfermeiro {
    private String nomeEnfermeiro, cpfEnfermeiro, rgEnfermeiro;
    private double salarioEnfermeiro;

    public String getNomeEnfermeiro() {
        return nomeEnfermeiro;
    }

    public void setNomeEnfermeiro(String nomeEnfermeiro) {
        this.nomeEnfermeiro = nomeEnfermeiro;
    }

    public String getCpfEnfermeiro() {
        return cpfEnfermeiro;
    }

    public void setCpfEnfermeiro(String cpfEnfermeiro) {
        this.cpfEnfermeiro = cpfEnfermeiro;
    }

    public String getRgEnfermeiro() {
        return rgEnfermeiro;
    }

    public void setRgEnfermeiro(String rgEnfermeiro) {
        this.rgEnfermeiro = rgEnfermeiro;
    }

    public double getSalarioEnfermeiro() {
        return salarioEnfermeiro;
    }

    public void setSalarioEnfermeiro(double salarioEnfermeiro) {
        this.salarioEnfermeiro = salarioEnfermeiro;
    }
    
    
}
